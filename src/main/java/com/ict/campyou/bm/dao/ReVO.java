package com.ict.campyou.bm.dao;

public class ReVO {
private String review_idx, review_comment, camp_site, rating, contentid;

public String getReview_idx() {
	return review_idx;
}

public void setReview_idx(String review_idx) {
	this.review_idx = review_idx;
}

public String getReview_comment() {
	return review_comment;
}

public void setReview_comment(String review_comment) {
	this.review_comment = review_comment;
}

public String getCamp_site() {
	return camp_site;
}

public void setCamp_site(String camp_site) {
	this.camp_site = camp_site;
}

public String getRating() {
	return rating;
}

public void setRating(String rating) {
	this.rating = rating;
}

public String getContentid() {
	return contentid;
}

public void setContentid(String contentid) {
	this.contentid = contentid;
}

}
